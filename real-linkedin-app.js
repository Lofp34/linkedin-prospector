// Cette application utilise les vrais outils MCP dans l'environnement Cursor

class RealLinkedInSearch {
    constructor() {
        this.searchHistory = [];
        this.lastSearchTime = null;
    }

    /**
     * Recherche r√©elle de profils LinkedIn
     * @param {string} firstName - Pr√©nom
     * @param {string} lastName - Nom de famille  
     * @param {Object} options - Options de recherche
     */
    async searchProfiles(firstName, lastName, options = {}) {
        try {
            console.log(`üîç RECHERCHE R√âELLE LINKEDIN: ${firstName} ${lastName}`);
            
            // Pr√©parer les param√®tres MCP
            const mcpParams = {
                first_name: firstName,
                last_name: lastName,
                count: options.count || 10,
                timeout: options.timeout || 300,
                ...options
            };

            console.log('üìã Param√®tres envoy√©s:', JSON.stringify(mcpParams, null, 2));

            // APPEL R√âEL √Ä L'OUTIL MCP
            const results = await mcp_hdw_search_linkedin_users(mcpParams);
            
            // Enregistrer dans l'historique
            this.searchHistory.push({
                timestamp: new Date(),
                params: mcpParams,
                resultCount: results.length
            });
            this.lastSearchTime = new Date();

            console.log(`‚úÖ R√âSULTATS TROUV√âS: ${results.length} profil(s)`);
            
            // Afficher les r√©sultats
            this.displayResults(results);
            
            return results;

        } catch (error) {
            console.error('‚ùå ERREUR RECHERCHE MCP:', error);
            throw error;
        }
    }

    /**
     * Obtenir les d√©tails complets d'un profil
     * @param {string} userIdentifier - URL, URN ou alias LinkedIn
     */
    async getProfileDetails(userIdentifier) {
        try {
            console.log(`üë§ R√âCUP√âRATION PROFIL R√âEL: ${userIdentifier}`);

            const mcpParams = {
                user: userIdentifier,
                with_experience: true,
                with_education: true,
                with_skills: true
            };

            // APPEL R√âEL √Ä L'OUTIL MCP
            const profile = await mcp_hdw_get_linkedin_profile(mcpParams);
            
            console.log('‚úÖ PROFIL R√âCUP√âR√â AVEC SUCC√àS');
            this.displayProfileDetails(profile);
            
            return profile;

        } catch (error) {
            console.error('‚ùå ERREUR R√âCUP√âRATION PROFIL:', error);
            throw error;
        }
    }

    /**
     * Recherche par email
     * @param {string} email - Adresse email
     */
    async searchByEmail(email) {
        try {
            console.log(`üìß RECHERCHE PAR EMAIL: ${email}`);

            const mcpParams = {
                email: email,
                count: 5,
                timeout: 300
            };

            // APPEL R√âEL √Ä L'OUTIL MCP
            const results = await mcp_hdw_get_linkedin_email_user(mcpParams);
            
            console.log(`‚úÖ TROUV√â ${results.length} profil(s) pour ${email}`);
            this.displayResults(results);
            
            return results;

        } catch (error) {
            console.error('‚ùå ERREUR RECHERCHE EMAIL:', error);
            throw error;
        }
    }

    /**
     * Recherche avanc√©e Sales Navigator
     * @param {Object} criteria - Crit√®res de recherche avanc√©e
     */
    async salesNavigatorSearch(criteria) {
        try {
            console.log('üéØ RECHERCHE SALES NAVIGATOR R√âELLE');
            console.log('Crit√®res:', JSON.stringify(criteria, null, 2));

            // APPEL R√âEL √Ä L'OUTIL MCP
            const results = await mcp_hdw_linkedin_sn_search_users(criteria);
            
            console.log(`‚úÖ SALES NAV: ${results.length} profil(s) trouv√©(s)`);
            this.displayResults(results);
            
            return results;

        } catch (error) {
            console.error('‚ùå ERREUR SALES NAVIGATOR:', error);
            throw error;
        }
    }

    /**
     * Afficher les r√©sultats de recherche
     */
    displayResults(results) {
        if (!results || results.length === 0) {
            console.log('üö´ Aucun r√©sultat trouv√©');
            return;
        }

        console.log('\nüìä === R√âSULTATS DE RECHERCHE ===');
        results.forEach((profile, index) => {
            console.log(`\n${index + 1}. üë§ ${profile.first_name || profile.firstName || 'N/A'} ${profile.last_name || profile.lastName || 'N/A'}`);
            console.log(`   üíº ${profile.headline || profile.current_position || 'Poste non sp√©cifi√©'}`);
            console.log(`   üè¢ ${profile.current_company || profile.company || 'Entreprise non sp√©cifi√©e'}`);
            console.log(`   üìç ${profile.location || 'Localisation non sp√©cifi√©e'}`);
            console.log(`   üë• ${profile.connections || 'N/A'} connexions`);
            
            if (profile.profile_url || profile.linkedin_url) {
                console.log(`   üîó ${profile.profile_url || profile.linkedin_url}`);
            }
            
            if (profile.urn) {
                console.log(`   üÜî URN: ${profile.urn}`);
            }
        });
        console.log('\n' + '='.repeat(40));
    }

    /**
     * Afficher les d√©tails d'un profil
     */
    displayProfileDetails(profile) {
        console.log('\nüë§ === D√âTAILS DU PROFIL ===');
        console.log(`üìõ Nom: ${profile.first_name || profile.firstName} ${profile.last_name || profile.lastName}`);
        console.log(`üíº Poste: ${profile.headline || profile.current_position}`);
        console.log(`üìç Localisation: ${profile.location}`);
        
        if (profile.summary) {
            console.log(`üìù R√©sum√©: ${profile.summary.substring(0, 200)}...`);
        }

        if (profile.experience && profile.experience.length > 0) {
            console.log('\nüíº EXP√âRIENCES:');
            profile.experience.slice(0, 3).forEach((exp, index) => {
                console.log(`   ${index + 1}. ${exp.title || exp.position} chez ${exp.company}`);
                console.log(`      üìÖ ${exp.duration || exp.period || 'P√©riode non sp√©cifi√©e'}`);
                if (exp.description) {
                    console.log(`      üìù ${exp.description.substring(0, 100)}...`);
                }
            });
        }

        if (profile.education && profile.education.length > 0) {
            console.log('\nüéì FORMATION:');
            profile.education.slice(0, 2).forEach((edu, index) => {
                console.log(`   ${index + 1}. ${edu.degree || edu.diploma} - ${edu.school || edu.institution}`);
                console.log(`      üìÖ ${edu.years || edu.period || 'P√©riode non sp√©cifi√©e'}`);
            });
        }

        if (profile.skills && profile.skills.length > 0) {
            console.log(`\nüõ†Ô∏è COMP√âTENCES: ${profile.skills.slice(0, 10).join(', ')}`);
        }

        if (profile.languages && profile.languages.length > 0) {
            console.log(`\nüåê LANGUES: ${profile.languages.join(', ')}`);
        }

        console.log('\n' + '='.repeat(40));
    }

    /**
     * Recherche avec crit√®res multiples
     */
    async searchWithCriteria(firstName, lastName, company = null, location = null, title = null, keywords = null) {
        const options = {};
        
        if (company) options.current_company = company;
        if (location) options.location = location;
        if (title) options.title = title;
        if (keywords) options.keywords = keywords;

        return await this.searchProfiles(firstName, lastName, options);
    }

    /**
     * Obtenir l'historique des recherches
     */
    getSearchHistory() {
        console.log('\nüìà === HISTORIQUE DES RECHERCHES ===');
        this.searchHistory.forEach((search, index) => {
            console.log(`${index + 1}. ${search.timestamp.toLocaleString()}`);
            console.log(`   Nom: ${search.params.first_name} ${search.params.last_name}`);
            console.log(`   R√©sultats: ${search.resultCount}`);
            if (search.params.current_company) {
                console.log(`   Entreprise: ${search.params.current_company}`);
            }
            if (search.params.location) {
                console.log(`   Localisation: ${search.params.location}`);
            }
            console.log('');
        });
        return this.searchHistory;
    }
}

// Fonctions utilitaires pour utilisation directe
async function rechercheRapide(prenom, nom, entreprise = null, ville = null) {
    const searcher = new RealLinkedInSearch();
    return await searcher.searchWithCriteria(prenom, nom, entreprise, ville);
}

async function profilDetaille(identifiant) {
    const searcher = new RealLinkedInSearch();
    return await searcher.getProfileDetails(identifiant);
}

async function rechercheEmail(email) {
    const searcher = new RealLinkedInSearch();
    return await searcher.searchByEmail(email);
}

async function rechercheAvancee(criteres) {
    const searcher = new RealLinkedInSearch();
    return await searcher.salesNavigatorSearch(criteres);
}

// Export pour utilisation
